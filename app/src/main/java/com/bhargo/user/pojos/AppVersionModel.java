package com.bhargo.user.pojos;

public class AppVersionModel {

    String strUserId;
    String strOrgId;
    String strAppName;
    String strOldVersion;
    String strNewVersion;

    public String getStrUserId() {
        return strUserId;
    }

    public void setStrUserId(String strUserId) {
        this.strUserId = strUserId;
    }

    public String getStrOrgId() {
        return strOrgId;
    }

    public void setStrOrgId(String strOrgId) {
        this.strOrgId = strOrgId;
    }

    public String getStrAppName() {
        return strAppName;
    }

    public void setStrAppName(String strAppName) {
        this.strAppName = strAppName;
    }

    public String getStrOldVersion() {
        return strOldVersion;
    }

    public void setStrOldVersion(String strOldVersion) {
        this.strOldVersion = strOldVersion;
    }

    public String getStrNewVersion() {
        return strNewVersion;
    }

    public void setStrNewVersion(String strNewVersion) {
        this.strNewVersion = strNewVersion;
    }
}
