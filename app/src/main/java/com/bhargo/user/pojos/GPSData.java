package com.bhargo.user.pojos;

public class GPSData {

    public String gps_data;
    public   String gps_altitude;
    public   String gps_accuracy;
    public   String gps_time;
    public   String max_accuracy;
    public   String max_time;
    public   String PROVIDER;

    public String getGps_data() {
        return gps_data;
    }

    public void setGps_data(String gps_data) {
        this.gps_data = gps_data;
    }

    public String getGps_altitude() {
        return gps_altitude;
    }

    public void setGps_altitude(String gps_altitude) {
        this.gps_altitude = gps_altitude;
    }

    public String getGps_accuracy() {
        return gps_accuracy;
    }

    public void setGps_accuracy(String gps_accuracy) {
        this.gps_accuracy = gps_accuracy;
    }

    public String getGps_time() {
        return gps_time;
    }

    public void setGps_time(String gps_time) {
        this.gps_time = gps_time;
    }

    public String getMax_accuracy() {
        return max_accuracy;
    }

    public void setMax_accuracy(String max_accuracy) {
        this.max_accuracy = max_accuracy;
    }

    public String getMax_time() {
        return max_time;
    }

    public void setMax_time(String max_time) {
        this.max_time = max_time;
    }

    public String getPROVIDER() {
        return PROVIDER;
    }

    public void setPROVIDER(String PROVIDER) {
        this.PROVIDER = PROVIDER;
    }
}
